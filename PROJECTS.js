function openNav() {
    document.getElementById("mySidenav").style.width = "170px";
    document.getElementById("main").style.marginLeft = "170px";
    
  }
  
  function closeNav() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("main").style.marginLeft = "0";
   
  }
  
  
  
